class InputForwarder(object):
    """
    Thif forwards and translates the keyboard and mouse events from a windows
    to the given input system.
    """
    
    def __init__(self, input_sys):
        self._input_sys = input_sys
        self._windows = set()
        
    def forward_window(self, window):
        if window not in self._windows:
            self._windows.add(window)
            
        window.Bind(wx.EVT_KEY_DOWN, self._on_key_down)
        window.Bind(wx.EVT_KEY_UP, self._on_key_up)
        
    def _map_key(self, evt):
        keycode = evt.GetKeyCode()
        keyname = keyMap.get(keycode, None)
        if keyname is None:
            if "unicode" in wx.PlatformInfo:
                keycode = evt.GetUnicodeKey()
                if keycode <= 127:
                    keycode = evt.GetKeyCode()
                keyname = "" + unichr(evt.GetUnicodeKey())
                if keycode < 27:
                    # For control keys (only happens on key char events)
                    # So we shouldn't have to deal with it, but we will just to
                    # be sure
                    keyname = "%s" % chr(ord('A') + keycode-1)
                
            elif keycode < 256:
                if keycode == 0:
                    keyname = "NUL"
                elif keycode < 27:
                    # For control keys (only happens on key char events)
                    # So we shouldn't have to deal with it, but we will just to
                    # be sure
                    keyname = "%s" % chr(ord('A') + keycode-1)
                else:
                    keyname = "%s" % chr(keycode)
            else:
                keyname = "unknown (%s)" % keycode
                
        return self._input_sys.key_string_to_key(keyname)
        
    def _on_key_up(self, key_event):
        key_code = self._map_key(key_event)
        if key_code is not None:
            self._input_sys.key_released(key_code, key_event.ControlDown(), 
                                         key_event.AltDown(), 
                                         key_event.ShiftDown(),
                                         key_event.MetaDown())
    
    def _on_key_down(self, key_event):
        key_code = self._map_key(key_event)
        if key_code is not None:
            self._input_sys.key_pressed(key_code, key_event.ControlDown(), 
                                        key_event.AltDown(), 
                                        key_event.ShiftDown(),
                                        key_event.MetaDown())